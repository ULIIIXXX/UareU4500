create table empleado(idEmpleado int not null auto_increment, nombre varchar(30), apPaterno varchar(30), apMaterno varchar(30), fingerprint blob,
	primary key(idEmpleado));

create table horario(idHorario int not null auto_increment, horaEntrada time, horaSalida time, horaComida time,
	primary key(idHorario));

create table vacaciones(idEmpleado int not null, fechaInicio date, fechaFin date,
	foreign key(idEmpleado) references empleado(idEmpleado),
	primary key(idEmpleado, fechaInicio, fechaFin));
	
create table descanso(idEmpleado int not null, fecha date,
	foreign key(idEmpleado) references empleado(idEmpleado),
	primary key(idEmpleado, fecha));
	
create table falta(idEmpleado int not null, idHorario int not null, fecha date,
	foreign key(idEmpleado) references empleado(idEmpleado),
	foreign key(idHorario) references horario(idHorario),
	primary key(idEmpleado, idHorario, fecha));
	
create table dia(idEmpleado int not null, idHorario int not null, fecha date, entrada time, salida time, salidaComida time, entradaComida time,
	foreign key(idEmpleado) references empleado(idEmpleado),
	foreign key(idHorario) references horario(idHorario),
	primary key(idEmpleado, idHorario, fecha));
	
